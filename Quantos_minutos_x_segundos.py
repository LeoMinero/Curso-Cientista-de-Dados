# Cálculo para descobrir quantos segundos tem x minutos

Seg1 = int(input('Digite um número: '))
Seg1 = Seg1 * 60
print('O número digitado equivale a ', Seg1, 'segundos.')

# Cálculo para descobrir quantos segundos tem x horas

Seg1 = int(input('Digite um número: '))
Seg1 = Seg1 * 60 * 60
print('O número digitado equivale a ', Seg1, 'segundos.')
